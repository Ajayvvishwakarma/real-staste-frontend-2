// Helper function to get city data based on property type
export const getCityData = (citySlug, propertyType = 'buy') => {
  const dataSource = propertyType === 'rent' ? cityRentPropertyData : cityBuyPropertyData;
  if (!citySlug) return dataSource.default;
  const cityKey = citySlug.toLowerCase().replace(/\s+/g, '').replace(/-/g, '');
  return dataSource[cityKey] || dataSource.default;
};

// Helper function to format city name for display
export const formatCityName = (citySlug) => {
  if (!citySlug) return "India";
  return citySlug
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
};

